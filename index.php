<h1>NetME</h1> 
<h2>On-the-fly knowledge network construction from biomedical literature</h2>
<h5>About the project</h5>
<p>The huge amount of biological literature, which daily increases, represents a strategic resource to automatically extract and gain knowledge concerning relations among biological elements. </p>
<p>Knowledge Networks are helpful tools in the context of biological knowledge discovery and modeling.</p>
<p>Here we introduce a novel system called NetMe, which, starting from a set of fulltext obtained from <a href="https://pubmed.ncbi.nlm.nih.gov/" target="_blank">PubMed</a>, through an easy-to-use web interface, interactively extracts a group of biological elements stored into a selected list of ontological databases and then synthesizes a network with inferred relations among such elements.</p>
<p>The results clearly show that our tool is capable to infer reliable functional biological networks in a reasonable time.</p>
